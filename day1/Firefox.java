package week1.day1;  

public class Firefox {

    public static void main(String[] args) {

        // Stores the browser version (decimal value)
        float browserVersion = 100.2f;

        // Stores the browser name
        String browserName = "firefox";

        // Stores whether the browser is visible (as String)
        String isVisible = "true";

        // Stores the year Firefox was released
        short releaseYear = 1998;

        // Stores the browser logo initial
        char browserLogo = 'f';

        // Print all values line by line using newline character
        System.out.println(browserVersion + "\n" + browserName + "\n" + isVisible + "\n" + releaseYear + "\n" + browserLogo);
    }
}
